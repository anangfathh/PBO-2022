public class Variabel {
    public static void main(String[] args) {
        // Deklarasikan variabel di sini, sesuai dengan tipe datanya
        int var1 = 123;
        double var2 = 243.584;
        String var3 = "Hello";
        

        // Cetak variabel di sini
        System.out.println(var1);
        System.out.println(var2);
        System.out.println(var3);
    }
}